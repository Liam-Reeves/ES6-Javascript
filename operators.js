    // Increment (++): Increases a number by 1.
    let x = 5;
    x++; 
    console.log(x);




    
    // Decrement (--): Decreases a number by 1.
    let y = 10;
    y--;
    console.log(y);







    
    // Exponentiation (**): Raises a number to a power.
    let z = 2 ** 3;
    console.log(z);



    // Bitwise operators:
    // Bitwise AND (&): Returns 1 if both bits are 1, otherwise 0.
    let a = 5 & 3;
    console.log(a);


    
    // Bitwise OR (|): Returns 1 if at least one bit is 1, otherwise 0. 
    let b = 5 | 3;
    console.log(b);


    
    // Bitwise XOR (^): Returns 1 if only one bit is 1, otherwise 0.
    let c = 5 ^ 3;
    console.log(c);


    
    // Bitwise NOT (~): Inverts all bits (0 becomes 1, 1 becomes 0).
    let d = ~5;
    console.log(d);
    

    // Bitwise Left Shift (<<): Shifts bits to the left by a specified number of places.
    let e = 5 << 1;
    console.log(e);
    
    // Bitwise Right Shift (>>): Shifts bits to the right by a specified number of places.
    let f = 5 >> 1;
    console.log(f);

    // Bitwise Unsigned Right Shift (>>>): Shifts bits to the right by a specified number of places, filling the leftmost bits with zeros.
    let g = 5 >>> 1;
    console.log(g);

    // Ternary operator: Conditional operator that returns one value or another based on a condition.
    let result = (age > 18) ? "Adult" : "Minor";
    console.log(result);
   
    // Comparison Operators

// Equal to (==): Checks if two values are equal.
    let isEqual = 5 == 5;
    console.log(isEqual);

    // Not equal to (!=): Checks if two values are not equal.
    let isNotEqual = 5!= 5;
    console.log(isNotEqual);

    // Strict equal to (===): Checks if two values are equal and of the same type.

    let isStrictlyEqual = 5 === 5;
    console.log(isStrictlyEqual);
    
    // Strict not equal to (!==): Checks if two values are not equal or of different types.
    let isStrictlyNotEqual = 5 !== 5;
    console.log(isStrictlyNotEqual);
    
    // Greater than (>): Checks if the left side value is greater than the right side value.
    let isGreaterThan = 5 > 3;
    console.log(isGreaterThan);
    
    // Less than (<): Checks if the left side value is less than the right side value.
    let isLessThan = 5 < 3;
    console.log(isLessThan);
    
    // Greater than or equal to (>=): Checks if the left side value is greater than or equal to the right side value.
    let isGreaterThanOrEqual = 5 >= 3;
    console.log(isGreaterThanOrEqual);
    
    // Less than or equal to (<=): Checks if the left side value is less than or equal to the right side value.
    let isLessThanOrEqual = 5 <= 3;
    console.log(isLessThanOrEqual);
    
                     // Logical operators
                     // Logical AND (&&): Returns true if both operands are true.
    let isAnd = true && true;
